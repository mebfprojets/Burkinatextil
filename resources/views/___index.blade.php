            <p style="display:inline-block;">
            Le Gouvernement du Burkina Faso a obtenu de son partenaire financier, la Banque Mondiale, un financement
            pour la mise en œuvre du Projet de Résilience et de Compétitivité Agricole <b>(PReCA)</b> qui est un projet intégré
            de soutien à la production agricole. Ce programme qui se veut ambitieux, s’inscrit dans une approche intégrée
            en cohérence avec les ambitions déclinées dans lePNDES. Les domaines prioritaires d’intervention de ce projets
            ont les aménagements hydroagricoles, le soutien à la production, l’entreprenariat agricole et le désenclavement
            des grandes zones de production.
            En termes de finalité, le PReCA qui traduit la vision du Gouvernement pour le secteur rural, vise une agriculture durable et compétitive,
            générant de la croissance économique et diversifiant les opportunités d'emploi, au profit de la population burkinabè.
            L’objectif de développement de ce projet est «d’accroître la productivité agricole et l'accès au marché pour les petits producteurs et les
            petits et moyens entrepreneurs agro-industriels (PME) pour les chaînes de valeur sélectionnées dans les zones de projet».
            Le PReCA est structuré en quatre (4) composantes:

            </p>
            -	
-	
-	

            <ul>
              <b><li><i class="ri-check-double-line"></i>Composante 1: Amélioration de la productivité agricole;</li>
              <li><i class="ri-check-double-line"></i>Composante 2: Amélioration de la compétitivité et facilitation de l'accès au marché;</li>
              <li><i class="ri-check-double-line"></i>Composante 3: Promotion et développement de l'agro-industrie par le secteur privé</li>
              <li><i class="ri-check-double-line"></i>Composante 4: Coordination du projet, renforcement des capacités institutionnelles et composante d'intervention d'urgence contingente</li>
             </ul></b>
            <p class="fst-italic">
            Lacomposante 3, dont l’objectif est de permettre au secteur privé agricole et de l'agro-transformation de devenir plus compétitif sur les marchés intérieurs et extérieurs, sera mise en œuvre à travers principalement
            l’octroi de financements par un mécanisme à couts partagés.
            </p>
